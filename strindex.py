#indexing = accessing elements of a sequence using [] (indexing starts at 0)

credit_number = "1234567890"

print(credit_number[0])  # First character
print(credit_number[:4])  # Second character
print(credit_number[1:5])  # Third character
print(credit_number[-3])
print(credit_number[::2])
print(credit_number[5:9])
print(credit_number[::])