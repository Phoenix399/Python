friends = 10
friends -= 2
print("After -= 2:", friends)

friends -= friends * 3
print("After -= friends * 3:", friends)

friends *= 3
print("After *= 3:", friends)

friends = friends / 2
print("After / 2:", friends)

friends /= 2
print("After /= 2:", friends)

friends = friends ** 2
print("After ** 2:", friends)

friends **= 2
print("After **= 2:", friends)

remainder = friends % 2
print("Remainder:", remainder)
